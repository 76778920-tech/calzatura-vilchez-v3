function zapSummary() {
  const counts = { altas: 0, medias: 0, bajas: 0, informativas: 0 };
  for (const site of zapReport?.site || []) {
    for (const alert of site.alerts || []) {
      const risk = String(alert.riskdesc || alert.risk || "");
      const count = (alert.instances || []).length || Number(alert.count) || 1;
      if (risk.startsWith("High")) counts.altas += count;
      else if (risk.startsWith("Medium")) counts.medias += count;
      else if (risk.startsWith("Low")) counts.bajas += count;
      else if (risk.startsWith("Informational")) counts.informativas += count;
    }
  }
  return `ZAP producción v2: ${counts.altas} alertas altas, ${counts.medias} medias, ${counts.bajas} bajas, ${counts.informativas} informativas. Resultado aceptado: sin alertas altas/críticas.`;
}

